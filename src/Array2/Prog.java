package Array2;

public class Prog 
{

	public static void main(String[] args)
	{
		//find number of arguments
		int n = args.length;
		System.out.println("No. of args= "+n);
		
		//display all arguments
		System.out.println("The args are: ");
		for (int i = 0;i<n;i++)
		System.out.println(args[i]);
		

	}

}
